/**
 * Order Status Constants Configuration
 * =====================================
 *
 * 이 파일은 주문 상태 관리를 위한 중앙 집중식 상수 정의입니다.
 * Notion 데이터베이스와 동기화되어 있습니다.
 *
 * ## 주요 구성요소
 *
 * 1. ORDER_STATUS_CODES: 모든 주문 상태의 숫자 코드 정의
 * 2. ORDER_STATUS_ENUM: 상태 코드와 한글 라벨 매핑
 * 3. ORDER_STATUS: 주문 처리 후 상태 목록 (UI 표시용)
 * 4. STATUS_COLORS: 상태별 색상 정의
 * 5. ORDER_STATUS_COLOR_ENUM: 상태 라벨과 색상 매핑
 *
 * ## 색상 그룹 분류
 *
 * - Orange (#FF8A01): 시작 전 상태 (WAITING)
 *   → 결제완료, 주문접수지연, 환불완료(검증실패), 테이블주문요청
 *
 * - Green (#3AA639): 진행 중 상태 (IN_PROGRESS)
 *   → 주문접수, 픽업대기, 배달진행, 환불처리 등
 *
 * - Blue (#0085E5): 완료 상태 (COMPLETED)
 *   → 픽업완료, 배달완료, 테이블주문취소
 *
 * - Red (#E3211E): 실패/오류 상태 (FAILED)
 *   → 배달실패, 결제실패, 환불실패, 주문요청실패
 *
 * - Grey (#8F8F8F): 환불 완료 상태 (REFUNDED)
 *   → 환불완료, 자동환불완료
 *
 * - 미표시: 색상 없음 (NONE)
 *   → 결제대기중, 테이블주문결제완료
 *
 * ## 상태 코드 범위
 *
 * - 0-10: 기본 주문 처리 (결제, 주문, 픽업, 배달)
 * - 11-14: 환불 처리
 * - 15-18: 결제 검증 및 지연 처리
 * - 19-22: 테이블 주문
 * - 23-29: 오류 및 실패 상태
 * - 30-31: 특수 상태 (환불실패, 로봇하차)
 * - 32-34: 부분 픽업
 *
 * @see Notion Database: https://www.notion.so/592dce08c9674ada85f75b9d6f04ea0d
 * @updated 2025-02-13
 */

