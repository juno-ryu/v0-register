interface StatusColorMapping {
  statuses: readonly OrderStatusCodeValue[]
  color: StatusColorValue
}

const STATUS_COLOR_MAPPINGS: Record<string, StatusColorMapping> = {
  WAITING: {
    statuses: WAITING_STATUS,
    color: STATUS_COLORS.WAITING,
  },
  IN_PROGRESS: {
    statuses: IN_PROGRESS_STATUS,
    color: STATUS_COLORS.IN_PROGRESS,
  },
  COMPLETED: {
    statuses: COMPLETED_STATUS,
    color: STATUS_COLORS.COMPLETED,
  },
  FAILED: {
    statuses: FAILED_STATUS,
    color: STATUS_COLORS.FAILED,
  },
  REFUNDED: {
    statuses: REFUNDED_STATUS,
    color: STATUS_COLORS.REFUNDED,
  },
  NONE: {
    statuses: NONE_STATUS,
    color: STATUS_COLORS.NONE,
  },
}

export const ORDER_STATUS_COLOR_ENUM: Record<string, StatusColorValue> = Object.entries(
  STATUS_COLOR_MAPPINGS,
).reduce(
  (acc, [_, { statuses, color }]) => ({
    ...acc,
    ...Object.fromEntries(
      statuses.map(status => [ORDER_STATUS_ENUM[status], color]),
    ),
  }),
  {},
)
